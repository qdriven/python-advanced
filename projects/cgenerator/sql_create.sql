CREATE TABLE `information_schema`.`COLUMNS`  (
  `TABLE_CATALOG` varchar(64) CHARACTER SET utf8 COLLATE utf8_bin NOT NULL,
  `TABLE_SCHEMA` varchar(64) CHARACTER SET utf8 COLLATE utf8_bin NOT NULL,
  `TABLE_NAME` varchar(64) CHARACTER SET utf8 COLLATE utf8_bin NOT NULL,
  `COLUMN_NAME` varchar(64) CHARACTER SET utf8 COLLATE utf8_tolower_ci NULL DEFAULT NULL,
  `ORDINAL_POSITION` int(10) UNSIGNED NOT NULL,
  `COLUMN_DEFAULT` text CHARACTER SET utf8 COLLATE utf8_bin NULL,
  `IS_NULLABLE` varchar(3) CHARACTER SET utf8 COLLATE utf8_general_ci NOT NULL DEFAULT '',
  `DATA_TYPE` longtext CHARACTER SET utf8 COLLATE utf8_bin NULL,
  `CHARACTER_MAXIMUM_LENGTH` bigint(21) NULL DEFAULT NULL,
  `CHARACTER_OCTET_LENGTH` bigint(21) NULL DEFAULT NULL,
  `NUMERIC_PRECISION` bigint(10) UNSIGNED NULL DEFAULT NULL,
  `NUMERIC_SCALE` bigint(10) UNSIGNED NULL DEFAULT NULL,
  `DATETIME_PRECISION` int(10) UNSIGNED NULL DEFAULT NULL,
  `CHARACTER_SET_NAME` varchar(64) CHARACTER SET utf8 COLLATE utf8_general_ci NULL DEFAULT NULL,
  `COLLATION_NAME` varchar(64) CHARACTER SET utf8 COLLATE utf8_general_ci NULL DEFAULT NULL,
  `COLUMN_TYPE` mediumtext CHARACTER SET utf8 COLLATE utf8_bin NOT NULL,
  `COLUMN_KEY` enum('','PRI','UNI','MUL') CHARACTER SET utf8 COLLATE utf8_bin NOT NULL,
  `EXTRA` varchar(256) CHARACTER SET utf8 COLLATE utf8_general_ci NULL DEFAULT NULL,
  `PRIVILEGES` varchar(154) CHARACTER SET utf8 COLLATE utf8_general_ci NULL DEFAULT NULL,
  `COLUMN_COMMENT` text CHARACTER SET utf8 COLLATE utf8_bin NOT NULL,
  `GENERATION_EXPRESSION` longtext CHARACTER SET utf8 COLLATE utf8_bin NOT NULL,
  `SRS_ID` int(10) UNSIGNED NULL DEFAULT NULL
);
